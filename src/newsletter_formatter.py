    @staticmethod
    def save_newsletter(html_content: str, output_path: str) -> None:
        """
        Save newsletter to file

        Args:
            html_content: Complete HTML newsletter
            output_path: Path to save the file
        """
        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(html_content)

    @staticmethod
    def extract_subject_line(content: str) -> str:
        """
        Extract subject line from newsletter content

        Args:
            content: Newsletter HTML content

        Returns:
            Subject line text
        """
        # Look for common subject line patterns
        if '<h1>' in content:
            start = content.find('<h1>') + 4
            end = content.find('</h1>', start)
            if end > start:
                return content[start:end].strip()

        return "Your Daily Newsletter"
